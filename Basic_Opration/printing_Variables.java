// This is a Java program that performs basic arithmetic operations on two numbers

public class printing_Variables
{

    public static void main(String[] args) 
    {
        // Initialize two variables 'a' and 'b' with values 9 and 6.9 respectively
        int a = 9;
        double b = 6.9;

        // Print the sum of 'a' and 'b'
        System.out.println(a + " + " + b + " = " + (a + b));

        // Print the difference of 'a' and 'b'
        System.out.println(a + " - " + b + " = " + (a - b));

        // Print the product of 'a' and 'b'
        System.out.println(a + " * " + b + " = " + (a * b));
    }
}