// This is a Java program that takes in two command-line arguments as strings
// and prints out the concatenation of the two strings
public class j5 {
  public static void main(String[] p) {
      // Get the first command-line argument and store it in variable 'str1'
      String str1 = p[0];

      // Get the second command-line argument and store it in variable 'str2'
      String str2 = p[1];

      // Print out the concatenation of the two strings
      System.out.println("Entered String: " + str1 + str2);
  }
}