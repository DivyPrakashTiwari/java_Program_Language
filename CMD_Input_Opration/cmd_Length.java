// This is a Java program that takes in a variable number of command-line arguments
// and prints out the number of arguments
public class j3 {
  public static void main(String[] len) {
      // Get the length of the array of command-line arguments and store it in variable 'lenth'
      int lenth = len.length;

      // Print out the number of command-line arguments
      System.out.println("Number of command-line arguments: " + lenth);
  }
}